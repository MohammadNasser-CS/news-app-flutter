class CardItem {
  final String image;
  final String category;
  final String title;
  final String ownerName;
  final String ownerImage;
  final String createdAt;

  CardItem({
    required this.image,
    required this.category,
    required this.title,
    required this.ownerName,
    required this.ownerImage,
    required this.createdAt,
  });
}
